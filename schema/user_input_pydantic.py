from pydantic import BaseModel,Field,field_validator
from typing import Annotated, Literal

# pydantic model to validate incoming data
class UserInput(BaseModel):

    age : Annotated[int,Field(...,gt=0,lt=120,description="Age of user")]
    income :Annotated[float,Field(...,description="income of user")]
    household_size: Annotated[int,Field(...,description="persons lives with user")]
    dependents: Annotated[int,Field(description="NO. people in a house who are dependent")]
    bmi : Annotated[float,Field(...,description="bmi of user, formula for calulation bmi= (weight/(height**2),2)")]                
    visits_last_year : Annotated[int,Field(...,description="how many time user hospital",examples=["0","1"])]
    hospitalizations_last_3yrs: Annotated[int,Field(...,description="do they hospitalize in last 3 years", examples=[0,1,2])]
    days_hospitalized_last_3yrs : Annotated[int,Field(...,description="how many days they hospitalize in hospital like 1 day or 2 day in numbers only",examples=[0,1,2])]
    medication_count : Annotated[int,Field(...,description="medication doctor refer for user or do they do medication in numbers only",examples=[0,1])]
    systolic_bp : Annotated[int,Field(...,description="maximum blood pressure ,'in numbers'",examples=[120,129,130])]
    diastolic_bp : Annotated[int,Field(...,description="minimum blood pressure,'in numbers'",examples=[120,129,130])]
    ldl : Annotated[float,Field(...,description="bad cholesterol",examples=[120.0,98.6,100])]
    hba1c : Annotated[float,Field(...,description="Hemoglobin A1c or glycated hemoglobin 'in numbers'",examples=[5.28,  4.82])]
    deductible : Annotated[float,Field(...,description="deductible is the amount you pay for covered services",examples=[500, 1000, 5000])]
    copay : Annotated[float,Field(...,description="A copay is a fixed amount (for example, $15) paid for a covered health care service or prescription, usually paid when receiving the service or medication",examples=[20, 10, 30, 50])]
    policy_term_years : Annotated[int,Field(...,description="the fixed number of years an insurance policy remains active",examples=[4,  1, 10,  5,  7,  3])]
    policy_changes_last_2yrs : Annotated[int,Field(...,description="policy is replaced or modified in some respect or repealed in parts",examples=[0,1,2])]
    provider_quality : Annotated[float,Field(...,description="preferences for a positive patient experience",examples=[3.73, 3.1 , 3.9 , 3.89])]
    risk_score : Annotated[float,Field(...,description="potential insured consumer's level of risk",examples=[0.5714, 1 , 0.2857, 0.8681])]
    chronic_count: Annotated[int,Field(...,description="white blood cell counts",examples=[1, 2, 3, 0, 4, 5, 6])]
    hypertension : Annotated[int,Field(...,examples=[0 ,1])]
    diabetes : Annotated[int,Field(...,description="in numbers yes or no ",examples=[0 ,1])]
    asthma : Annotated[int,Field(...,examples=[0 ,1])]
    copd : Annotated[int,Field(...,examples=[0 , 1])]
    cardiovascular_disease : Annotated[int,Field(...,examples=[0, 1])]
    cancer_history : Annotated[int,Field(...,examples=[0 ,1])]
    kidney_disease : Annotated[int,Field(...,examples=[0, 1])]
    liver_disease : Annotated[int,Field(...,examples=[0, 1])]
    arthritis : Annotated[int,Field(...,examples=[0, 1])]
    mental_health : Annotated[int,Field(...,examples=[0, 1])]
    proc_imaging_count : Annotated[int,Field(...,description="undergone imaging procedures, such as X-rays, CT scans, or MRIs",examples=[0, 1, 2, 3])]
    proc_surgery_count : Annotated[int,Field(...,examples=[0 , 1 ,2 ,3, 4])]
    proc_physio_count : Annotated[int,Field(...,examples=[0 , 1 ,2 ,3, 4])]
    proc_consult_count : Annotated[int,Field(...,examples=[0 , 1 ,2 ,3, 4])]
    proc_lab_count: Annotated[int,Field(...,examples=[0 , 1 ,2 ,3, 4])]
    had_major_procedure : Annotated[int,Field(...,examples=[0 , 1])]
    sex : Annotated[Literal["male","female","others"],Field(...,description="Gender of the user")]
    region: Annotated[str,Field(...,examples=['North', 'Central', 'West', 'South', 'East'])]
    urban_rural : Annotated[str,Field(...,examples=["Suburban", "Urban", "Rural"])]
    education : Annotated[str,Field(...,examples=['Doctorate', 'No HS', 'HS', 'Some College', 'Masters', 'Bachelors'])]
    marital_status : Annotated[str,Field(...,examples=['Married', 'unknown', 'Single', 'Divorced', 'Widowed'])]
    employment_status : Annotated[str,Field(...,examples=['Retired', 'Employed', 'unknown', 'Self-employed', 'Unemployed'])]
    smoker : Annotated[str,Field(...,examples=['Never', 'unknown', 'Current', 'Former'])]
    alcohol_freq :Annotated[str,Field(...,examples=['unknown', 'Weekly', 'Daily', 'Occasional'])]
    plan_type :Annotated[str,Field(...,description="different types of health insurance plans that offer different coverage for doctors, hospitals and health providers",examples=['PPO', 'POS', 'HMO', 'EPO'])]
    network_tier: Annotated[str,Field(...,description="different cost-sharing levels",examples=['Bronze', 'Gold', 'Platinum', 'Silver'])]
